#---------------------------------------------------------------------
# Function: CheckLinux
#    Check Installed Linux Version
#---------------------------------------------------------------------

CheckLinux() {

  #Extract information on system
  . /etc/os-release

  # Set DISTRO variable to null
  DISTRO=''

  #---------------------------------------------------------------------
  #    Debian 7 Wheezy
  #---------------------------------------------------------------------
  
  if echo $ID-$VERSION_ID | grep -iq "debian-7"; then
		DISTRO=debian7
  fi
  

  #---------------------------------------------------------------------
  #    Debian 8 Jessie
  #---------------------------------------------------------------------
  
  if echo $ID-$VERSION_ID | grep -iq "debian-8"; then
		DISTRO=debian8
  fi

  #---------------------------------------------------------------------
  #    Debian 9 Stretch
  #---------------------------------------------------------------------
  
  if echo $ID-$VERSION_ID | grep -iq "debian-9"; then
		DISTRO=debian9
  fi
  
  #---------------------------------------------------------------------
  #    Ubuntu 14.04
  #---------------------------------------------------------------------
  
  if echo $ID-$VERSION_ID | grep -iq "ubuntu-14.04"; then
		DISTRO=ubuntu-14.04
  fi

  #---------------------------------------------------------------------
  #	Ubuntu 15.10
  #---------------------------------------------------------------------

  if echo $ID-$VERSION_ID | grep -iq "ubuntu-15.10"; then
		DISTRO=ubuntu-15.10
  fi
  
  #---------------------------------------------------------------------
  #	Ubuntu 16.04
  #---------------------------------------------------------------------

  if echo $ID-$VERSION_ID | grep -iq "ubuntu-16.04"; then
		DISTRO=ubuntu-16.04
  fi
  
  #---------------------------------------------------------------------
  #	Ubuntu 16.10
  #---------------------------------------------------------------------

  if echo $ID-$VERSION_ID | grep -iq "ubuntu-16.10"; then
		DISTRO=ubuntu-16.10
  fi
  
  #---------------------------------------------------------------------
  #     Ubuntu 18.04
  #---------------------------------------------------------------------

  if echo $ID-$VERSION_ID | grep -iq "ubuntu-18.04"; then
                DISTRO=ubuntu-18.04
  fi

  #---------------------------------------------------------------------
  #    CentOS
  #---------------------------------------------------------------------

  if echo $ID-$VERSION_ID | grep -iq "centos-7"; then
		DISTRO=centos7
  fi

}

